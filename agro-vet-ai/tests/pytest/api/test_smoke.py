"""
Детерминированные smoke-тесты для проверки основных сценариев работы системы.

Категории:
- Общие вопросы (capabilities, нерелевантный вопрос)
- Дифференциальная диагностика болезней свиней (симптомы → диагноз)
- Дифференциальная диагностика болезней кур (симптомы → диагноз)
- Вопросы к librarian (поиск по книгам)
- Вопросы к pharmacist (конкретные препараты по торговому названию / МНН)
"""

from utils import submit
from metrics import assert_contains

# ============================================================
# Общие вопросы
# ============================================================

class TestGeneralQuestions:
    """Общие вопросы: capabilities и нерелевантный запрос."""

    def test_capabilities(self, api_config):
        """Бот должен описать свои возможности."""
        data = submit(api_config, "Что ты умеешь?")
        assert_contains(data, [
            "ветер", "ветеринар", "помочь", "могу", "умею"
        ])


# ============================================================
# Дифференциальная диагностика болезней свиней
# Вопросы описывают симптомы → бот должен предложить диагноз
# ============================================================

class TestSwineDiagnosis:
    """Дифференциальная диагностика болезней свиней по симптомам."""

    def test_pseudorabies_symptoms(self, api_config):
        """Симптомы, характерные для болезни Ауески (псевдобешенство)."""
        data = submit(
            api_config,
            "У поросят-отъёмышей температура 41°C, судороги, манежные движения, "
            "скрежет зубами, паралич конечностей, слюнотечение. Какой предварительный диагноз?"
        )
        assert_contains(data, [
            "ауески", "ауэски", "псевдобешенств", "герпес",
        ])


# ============================================================
